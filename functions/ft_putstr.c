#include "../mandatory/push_swap.h"

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			write(0, &s[i], 1);
			i++;
		}
	}
	write(0, "\n", 1);
}
